# Dengan def keyword
def add(x,y):
    return x + y

print("total dari 100 dan 200 adalah:",add(100,200))

#output
# total dari 100 dan 200 adalah: 300

# Dengan lambda function
add = lambda x, y : x + y
print("total dari 100 dan 200 adalah:",add(100,200))

#output
# total dari 100 dan 200 adalah: 300