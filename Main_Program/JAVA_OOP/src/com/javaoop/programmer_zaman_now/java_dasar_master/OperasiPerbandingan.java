package com.javaoop.programmer_zaman_now.java_dasar_master;
public class OperasiPerbandingan {
  public static void main(String[] args) {

    int a = 100;
    int b = 100;

    System.out.println(a > b);
    System.out.println(a < b);
    System.out.println(a >= b);
    System.out.println(a <= b);
    System.out.println(a == b);
    System.out.println(a != b);

  }
}
